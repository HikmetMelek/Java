		boolean isMarried=true;
				
		Scanner scan = new Scanner(System.in);
			
        System.out.println("Are you married?");
        isMarried=scan.nextBoolean();
               
        if (isMarried) {
        	System.out.println("Married?: "+ "true");
        }else {
        	System.out.println("Married?: "+ "false");
        }
        
        
        
        
        
        
        
        
        
        
        
        
