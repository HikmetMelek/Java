package lessons_EU7.day2_printing_comments;

public class Concatenation {

	public static void main(String[] args) {
		
		System.out.println("Hello, my number is 10");
		System.out.println("Hello, my number is " + "10");
		System.out.println("Hello, my number is " + 10);
		
		int number = 10;
		System.out.println("Hello, my number is " + number);
		

	}

}
